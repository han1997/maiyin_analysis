export function getDemoDetail(personKey: string): PersonDetail {
  const person = demoPeople.find((item) => item.personKey === personKey) ?? demoPeople[0];
  if (!person) {
    throw new Error("演示数据中没有可用人员");
  }

  if (person.personKey === demoPeople[0]?.personKey) {
    return { person, alerts, evidence };
  }

  return {
    person,
    alerts: person.alertTitles.map((title, index) => ({
      kind: index === 0 ? "month_frequency" : "year_frequency",
      severity: person.level === "高风险" ? "高" : "中",
      score: Math.min(person.score, index === 0 ? 45 : 38),
      title,
      detail: "该说明由演示适配器生成。Tauri 模式将展示 Rust 分析返回的完整规则说明和证据行。",
      evidenceCount: Math.min(person.totalRecords, index === 0 ? person.maxMonthCount : person.maxYearCount),
    })),
    evidence: evidence.slice(0, Math.min(evidence.length, Math.max(1, person.alertCount))),
  };
}
